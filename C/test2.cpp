#include <stdio.h>

int main ()
{
	int octal=012;
	int hex=0xa2;	
	printf ("The octal assigning value is...:");
	printf("\n%o\n",octal);
	printf ("The decimal corresponding value is...:");
	printf("\n%d",octal);
	printf ("The hex assigning value is...:");
	printf("\n%x\n",hex);
	printf ("The decimal corresponding value is...:");
	printf("\n%d",hex);
	return 0;
}
